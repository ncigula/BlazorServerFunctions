using System.Text;
using BlazorServerFunctions.Generator.Models;

namespace BlazorServerFunctions.Generator.Generators;

internal static class ClientRegistrationGenerator
{
    public static string Generate(IReadOnlyCollection<InterfaceInfo> interfaces)
    {
        if (interfaces.Count == 0)
            return string.Empty;

        var generatedNamespace = FindCommonNamespace(interfaces);
        var sb = new StringBuilder();

        sb.AppendLine("// <auto-generated />");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using Microsoft.Extensions.DependencyInjection;");

        foreach (var ns in interfaces
            .Select(i => i.Namespace)
            .Distinct(StringComparer.Ordinal)
            .Where(ns => !string.Equals(ns, generatedNamespace, StringComparison.Ordinal))
            .OrderBy(ns => ns, StringComparer.Ordinal))
        {
            sb.Append("using ").Append(ns).AppendLine(";");
        }

        sb.AppendLine();
        sb.Append("namespace ").Append(generatedNamespace).AppendLine(";");
        sb.AppendLine();
        sb.AppendLine("public static class ServerFunctionClientsRegistration");
        sb.AppendLine("{");
        sb.AppendLine("    public static IServiceCollection AddServerFunctionClients(");
        sb.AppendLine("        this IServiceCollection services)");
        sb.AppendLine("    {");

        foreach (var interfaceName in interfaces.Select(i => i.Name))
        {
            var clientClassName = interfaceName.TrimStart('I') + "Client";

            sb.Append("        services.AddHttpClient<")
                .Append(interfaceName)
                .Append(", ")
                .Append(clientClassName)
                .AppendLine(">();");
        }

        sb.AppendLine();
        sb.AppendLine("        return services;");
        sb.AppendLine("    }");
        sb.AppendLine("}");

        return sb.ToString();
    }

    private static string FindCommonNamespace(IReadOnlyCollection<InterfaceInfo> interfaces)
    {
        var namespaces = interfaces.Select(i => i.Namespace).Distinct(StringComparer.Ordinal).ToList();
        if (namespaces.Count == 1)
            return namespaces[0];

        var parts = namespaces.Select(ns => ns.Split('.')).ToList();
        var common = new List<string>();
        var minLength = parts.Min(p => p.Length);

        for (var i = 0; i < minLength; i++)
        {
            var segment = parts[0][i];
            if (parts.All(p => string.Equals(p[i], segment, StringComparison.Ordinal)))
                common.Add(segment);
            else
                break;
        }

        return common.Count > 0 ? string.Join(".", common) : "BlazorServerFunctions.Generated";
    }
}
