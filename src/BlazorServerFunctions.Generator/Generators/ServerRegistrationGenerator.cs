using System.Collections.Generic;
using System.Linq;
using System.Text;
using BlazorServerFunctions.Generator.Models;

namespace BlazorServerFunctions.Generator.Generators;

internal static class ServerRegistrationGenerator
{
    public static string Generate(IReadOnlyCollection<InterfaceInfo> interfaces, string? targetNamespace)
    {
        var restInterfaces = interfaces.Where(i => i.Configuration.ApiType != ApiType.GRPC).ToList();
        var grpcInterfaces = interfaces.Where(i => i.Configuration.ApiType == ApiType.GRPC).ToList();

        if (restInterfaces.Count == 0 && grpcInterfaces.Count == 0)
            return string.Empty;

        // Use REST namespace first, fall back to gRPC namespace
        string fallbackNs = restInterfaces.Count > 0 ? restInterfaces[0].Namespace : grpcInterfaces[0].Namespace;
        var ns = !string.IsNullOrEmpty(targetNamespace) ? targetNamespace : fallbackNs;

        var externalNamespaces = interfaces
            .Select(i => i.Namespace)
            .Where(n => !string.Equals(n, ns, StringComparison.Ordinal) && !string.IsNullOrEmpty(n))
            .Distinct(StringComparer.Ordinal)
            .OrderBy(n => n, StringComparer.Ordinal)
            .ToList();

        var sb = new StringBuilder();

        sb.AppendLine("// <auto-generated />");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using Microsoft.AspNetCore.Builder;");
        sb.AppendLine("using Microsoft.AspNetCore.Diagnostics.HealthChecks;");
        sb.AppendLine("using Microsoft.AspNetCore.Routing;");
        sb.AppendLine("using Microsoft.Extensions.DependencyInjection;");
        sb.AppendLine("using Microsoft.Extensions.Diagnostics.HealthChecks;");
        // MapGrpcService<T> is an extension on IEndpointRouteBuilder from Grpc.AspNetCore.Server,
        // resolved via the Microsoft.AspNetCore.App framework reference — no extra using needed.
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Threading;");
        sb.AppendLine("using System.Threading.Tasks;");

        foreach (var externalNs in externalNamespaces)
            sb.Append("using ").Append(externalNs).AppendLine(";");

        sb.AppendLine();
        if (!string.IsNullOrEmpty(ns))
        {
            sb.Append("namespace ").Append(ns).AppendLine(";");
            sb.AppendLine();
        }

        sb.AppendLine("public static class ServerFunctionEndpointsRegistration");
        sb.AppendLine("{");
        GenerateMapEndpointsMethod(sb, restInterfaces, grpcInterfaces);
        GenerateAddHealthChecksMethod(sb, interfaces);
        GenerateMapHealthChecksMethod(sb);
        sb.AppendLine("}");
        sb.AppendLine();
        GenerateResolveCheckHelper(sb);

        return sb.ToString();
    }

    private static void GenerateMapEndpointsMethod(
        StringBuilder sb,
        List<InterfaceInfo> restInterfaces,
        List<InterfaceInfo> grpcInterfaces)
    {
        sb.AppendLine("    public static IEndpointRouteBuilder MapServerFunctionEndpoints(");
        sb.AppendLine("        this IEndpointRouteBuilder endpoints)");
        sb.AppendLine("    {");

        foreach (var interfaceInfo in restInterfaces)
        {
            sb.Append("        endpoints.Map")
                .Append(interfaceInfo.Name)
                .AppendLine("Endpoints();");
        }

        foreach (var grpcInterface in grpcInterfaces)
        {
            var serviceClassName = grpcInterface.Name.TrimStart('I') + "GrpcService";
            sb.Append("        endpoints.MapGrpcService<")
                .Append(serviceClassName)
                .AppendLine(">().EnableGrpcWeb();");
        }

        sb.AppendLine("        return endpoints;");
        sb.AppendLine("    }");
    }

    private static void GenerateAddHealthChecksMethod(StringBuilder sb, IReadOnlyCollection<InterfaceInfo> interfaces)
    {
        sb.AppendLine();
        sb.AppendLine("    public static IServiceCollection AddServerFunctionHealthChecks(");
        sb.AppendLine("        this IServiceCollection services)");
        sb.AppendLine("    {");
        sb.Append("        services.AddHealthChecks()");

        var list = interfaces.ToList();
        for (var i = 0; i < list.Count; i++)
        {
            var tag = DeriveTag(list[i].Name);
            sb.AppendLine();
            sb.AppendLine("            .Add(new HealthCheckRegistration(");
            sb.Append("                \"").Append(tag).AppendLine("\",");
            sb.Append("                sp => new __BsfResolveCheck<").Append(list[i].Name).AppendLine(">(sp),");
            sb.AppendLine("                HealthStatus.Unhealthy,");
            sb.Append("                tags: new[] { \"bsf\" }))");
            if (i == list.Count - 1)
                sb.AppendLine(";");
        }

        sb.AppendLine("        return services;");
        sb.AppendLine("    }");
    }

    private static void GenerateMapHealthChecksMethod(StringBuilder sb)
    {
        sb.AppendLine();
        sb.AppendLine("    public static IEndpointRouteBuilder MapServerFunctionHealthChecks(");
        sb.AppendLine("        this IEndpointRouteBuilder endpoints,");
        sb.AppendLine("        string pattern = \"/health/server-functions\")");
        sb.AppendLine("    {");
        sb.AppendLine("        endpoints.MapHealthChecks(pattern, new HealthCheckOptions");
        sb.AppendLine("        {");
        sb.AppendLine("            Predicate = check => check.Tags.Contains(\"bsf\"),");
        sb.AppendLine("        });");
        sb.AppendLine("        return endpoints;");
        sb.AppendLine("    }");
    }

    private static void GenerateResolveCheckHelper(StringBuilder sb)
    {
        sb.AppendLine("internal sealed class __BsfResolveCheck<TService> : IHealthCheck");
        sb.AppendLine("    where TService : class");
        sb.AppendLine("{");
        sb.AppendLine("    private readonly IServiceProvider _sp;");
        sb.AppendLine("    internal __BsfResolveCheck(IServiceProvider sp) => _sp = sp;");
        sb.AppendLine();
        sb.AppendLine("    public Task<HealthCheckResult> CheckHealthAsync(");
        sb.AppendLine("        HealthCheckContext context,");
        sb.AppendLine("        CancellationToken cancellationToken = default)");
        sb.AppendLine("    {");
        sb.AppendLine("        try");
        sb.AppendLine("        {");
        sb.AppendLine("            using var scope = _sp.CreateScope();");
        sb.AppendLine("            _ = scope.ServiceProvider.GetRequiredService<TService>();");
        sb.AppendLine("            return Task.FromResult(HealthCheckResult.Healthy());");
        sb.AppendLine("        }");
        sb.AppendLine("        catch (Exception ex)");
        sb.AppendLine("        {");
        sb.AppendLine("            return Task.FromResult(HealthCheckResult.Unhealthy(ex.Message, ex));");
        sb.AppendLine("        }");
        sb.AppendLine("    }");
        sb.AppendLine("}");
    }

    private static string DeriveTag(string interfaceName) =>
        interfaceName.Length > 1 && interfaceName[0] == 'I' && char.IsUpper(interfaceName[1])
            ? interfaceName.Substring(1)
            : interfaceName;
}
