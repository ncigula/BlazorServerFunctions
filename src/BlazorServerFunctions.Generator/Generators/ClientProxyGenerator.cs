using System.Text;
using BlazorServerFunctions.Generator.Helpers;
using BlazorServerFunctions.Generator.Models;
using HttpMethod = BlazorServerFunctions.Generator.Models.HttpMethod;

namespace BlazorServerFunctions.Generator.Generators;

internal static class ClientProxyGenerator
{
    public static string Generate(InterfaceInfo interfaceInfo)
    {
        var sb = new StringBuilder();

        var config = interfaceInfo.Configuration;
        var httpClientType = config.CustomHttpClientType ?? "HttpClient";
        var baseRoute = $"/{config.BaseRoute.TrimStart('/')}/{interfaceInfo.RoutePrefix}";

        sb.AppendLine("// <auto-generated />");
        if (config.Nullable)
            sb.AppendLine("#nullable enable");
        sb.AppendLine();

        var hasCancellationToken = interfaceInfo.Methods.Any(m => m.HasCancellationToken);
        var hasStreaming = interfaceInfo.Methods.Any(m => m.AsyncType is AsyncType.AsyncEnumerable);
        AddUsingDirectives(sb: sb, hasCancellationToken: hasCancellationToken, hasStreaming: hasStreaming);

        sb.Append("namespace ").Append(interfaceInfo.Namespace).Append(';').AppendLine();
        sb.AppendLine();
        sb.Append("public class ").Append(interfaceInfo.Name.TrimStart('I')).Append("Client : ").Append(interfaceInfo.Name).AppendLine();
        sb.AppendLine("{");
        sb.Append("    private readonly ").Append(httpClientType).AppendLine(" _httpClient;");
        sb.AppendLine($"    private const string BaseRoute = \"{baseRoute}\";");
        sb.AppendLine();

        AddConstructor(sb: sb, name: interfaceInfo.Name, httpClientType: httpClientType);

        sb.AppendLine();

        foreach (var method in interfaceInfo.Methods)
        {
            GenerateClientMethod(sb, method, config.RouteNaming);
        }

        sb.AppendLine("}");

        return sb.ToString();
    }

    private static void AddConstructor(StringBuilder sb, string name, string httpClientType)
    {
        sb.Append($"    public {name.TrimStart('I')}Client(").Append(httpClientType).AppendLine(" httpClient)");
        sb.AppendLine("    {");
        sb.AppendLine("        _httpClient = httpClient;");
        sb.AppendLine("    }");
    }

    private static void AddUsingDirectives(StringBuilder sb, bool hasCancellationToken, bool hasStreaming)
    {
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Net;");
        sb.AppendLine("using System.Net.Http;");
        sb.AppendLine("using System.Net.Http.Json;");
        if (hasStreaming)
        {
            sb.AppendLine("using System.Collections.Generic;");
            sb.AppendLine("using System.Runtime.CompilerServices;");
        }

        // Streaming methods always add a CancellationToken parameter even when the interface omits one
        if (hasCancellationToken || hasStreaming)
            sb.AppendLine("using System.Threading;");
        sb.AppendLine("using System.Threading.Tasks;");
        sb.AppendLine();
    }

    private static string GetAwaitKeyword(AsyncType asyncType) =>
        asyncType is not AsyncType.None ? "await " : string.Empty;

    private static string GetResultSuffix(AsyncType asyncType) =>
        asyncType is AsyncType.None ? ".GetAwaiter().GetResult()" : string.Empty;

    /// <summary>
    /// Strips route constraints from route parameter tokens so they are valid C# interpolation expressions.
    /// For example: {id:int} → {id}, {slug:minlength(3)} → {slug}, {id?} → {id}.
    /// </summary>
    private static string RouteTemplateToInterpolation(string routeTemplate) =>
        RegexExpressions.RouteParameterRegex.Replace(routeTemplate, static m => "{" + m.Groups["name"].Value + "}");

    private static void GenerateClientMethod(StringBuilder sb, MethodInfo methodInfo, RouteNaming routeNaming)
    {
        GenerateMethodSignature(
            sb: sb,
            method: methodInfo);

        sb.AppendLine("    {");

        if (methodInfo.AsyncType is AsyncType.AsyncEnumerable)
        {
            GenerateStreamingClientMethodBody(sb, methodInfo, routeNaming);
            sb.AppendLine("    }");
            sb.AppendLine();
            return;
        }

        var nonRouteParams = methodInfo.Parameters.Where(static p => !p.IsRouteParameter).ToList();
        var hasNonRouteParams = nonRouteParams.Count > 0;

        if (hasNonRouteParams && methodInfo.HttpMethod is HttpMethod.Post or HttpMethod.Put or HttpMethod.Patch)
            BuildRequestObject(sb: sb, parameters: nonRouteParams);

        GenerateHttpRequest(
            sb: sb,
            method: methodInfo,
            nonRouteParams: nonRouteParams,
            routeNaming: routeNaming);

        sb.AppendLine();
        sb.AppendLine("        if (!response.IsSuccessStatusCode)");
        sb.AppendLine("        {");
        sb.Append("            var errorBody = ")
            .Append(GetAwaitKeyword(methodInfo.AsyncType))
            .Append("response.Content.ReadAsStringAsync(");
        if (methodInfo.HasCancellationToken)
            sb.Append("cancellationToken");
        sb.Append(')').Append(GetResultSuffix(methodInfo.AsyncType)).AppendLine(";");
        sb.AppendLine("            throw new HttpRequestException(errorBody, null, response.StatusCode);");
        sb.AppendLine("        }");
        sb.AppendLine();

        if (!string.Equals(methodInfo.ReturnType, "void", StringComparison.OrdinalIgnoreCase))
        {
            // Use a nullable type argument so that ?? throw works for both value types
            // (where ReadFromJsonAsync<T> may return T in .NET 10+) and reference types.
            var nullableReturnType = methodInfo.ReturnType.EndsWith("?", StringComparison.Ordinal)
                ? methodInfo.ReturnType
                : methodInfo.ReturnType + "?";

            sb.Append("        return ")
                .Append(GetAwaitKeyword(methodInfo.AsyncType))
                .Append("response.Content.ReadFromJsonAsync<")
                .Append(nullableReturnType)
                .Append(">()")
                .Append(GetResultSuffix(methodInfo.AsyncType))
                .AppendLine();
            sb.AppendLine("            ?? throw new InvalidOperationException(\"Response deserialization returned null\");");
        }

        sb.AppendLine("    }");
        sb.AppendLine();
    }

    private static void BuildRequestObject(StringBuilder sb, List<ParameterInfo> parameters)
    {
        sb.AppendLine("        var request = new");
        sb.AppendLine("        {");

        foreach (var parameterName in parameters.Select(p => p.Name))
        {
            sb.Append("            ")
                .Append(parameterName.ToPascalCase())
                .Append(" = ")
                .Append(parameterName)
                .AppendLine(",");
        }

        sb.AppendLine("        };");
        sb.AppendLine();
    }

    private static void GenerateMethodSignature(
        StringBuilder sb,
        MethodInfo method)
    {
        sb.Append("    public ");

        if (method.AsyncType is AsyncType.AsyncEnumerable)
            sb.Append("async IAsyncEnumerable<").Append(method.ReturnType).Append('>');
        else if (method.AsyncType is not AsyncType.None)
        {
            sb.Append("async ").Append(method.AsyncType.ToString());
            if (!string.Equals(method.ReturnType, "void", StringComparison.Ordinal))
                sb.Append('<').Append(method.ReturnType).Append('>');
        }
        else
            sb.Append(method.ReturnType);

        sb.Append(' ').Append(method.Name).Append('(');

        var paramDeclarations = method.Parameters.Select(p =>
        {
            var declaration = $"{p.Type} {p.Name}";
            if (p.HasDefaultValue)
            {
                declaration += $" = {FormatDefaultValue(p.Type, p.DefaultValue)}";
            }

            return declaration;
        }).ToList();

        if (method.AsyncType is AsyncType.AsyncEnumerable)
        {
            // Streaming methods always expose [EnumeratorCancellation] CancellationToken
            if (paramDeclarations.Count > 0)
                sb.Append(string.Join(", ", paramDeclarations)).Append(", ");
            sb.Append("[EnumeratorCancellation] CancellationToken cancellationToken = default");
        }
        else if (method.HasCancellationToken)
        {
            if (paramDeclarations.Count > 0)
                sb.Append(string.Join(", ", paramDeclarations)).Append(", CancellationToken cancellationToken = default");
            else
                sb.Append("CancellationToken cancellationToken = default");
        }
        else
        {
            sb.Append(string.Join(", ", paramDeclarations));
        }

        sb.AppendLine(")");
    }

    private static void GenerateHttpRequest(StringBuilder sb, MethodInfo method, List<ParameterInfo> nonRouteParams, RouteNaming routeNaming)
    {
        // When the user specified an explicit Route, use it verbatim (stripping constraints for C# interpolation).
        // ApplyRouteNaming is only applied to auto-derived method names.
        var urlSegment = method.CustomRoute != null
            ? RouteTemplateToInterpolation(method.CustomRoute)
            : method.Name.ApplyRouteNaming(routeNaming);

        var hasNonRouteParams = nonRouteParams.Count > 0;

        switch (method.HttpMethod)
        {
            case HttpMethod.Post:
                GeneratePostRequest(sb, urlSegment, hasNonRouteParams, method.AsyncType, method.HasCancellationToken);
                break;

            case HttpMethod.Get:
            case HttpMethod.Delete:
                GenerateQueryStringRequest(sb, urlSegment, method.HttpMethod, nonRouteParams, method.AsyncType, method.HasCancellationToken);
                break;

            default:
                GenerateOtherRequest(sb, urlSegment, method.HttpMethod, hasNonRouteParams, method.AsyncType, method.HasCancellationToken);
                break;
        }
    }

    private static void GeneratePostRequest(StringBuilder sb, string urlSegment, bool hasParameters, AsyncType asyncType, bool hasCancellationToken)
    {
        var awaitKeyword = GetAwaitKeyword(asyncType);
        var resultSuffix = GetResultSuffix(asyncType);

        if (hasParameters)
        {
            sb.AppendLine($"        var response = {awaitKeyword}_httpClient.PostAsJsonAsync(");
            sb.Append("            $\"{BaseRoute}/").Append(urlSegment).AppendLine("\",");
            if (hasCancellationToken)
            {
                sb.AppendLine("            request,");
                sb.AppendLine($"            cancellationToken){resultSuffix};");
            }
            else
            {
                sb.AppendLine($"            request){resultSuffix};");
            }
        }
        else
        {
            sb.AppendLine($"        var response = {awaitKeyword}_httpClient.PostAsync(");
            sb.Append("            $\"{BaseRoute}/").Append(urlSegment).AppendLine("\",");
            if (hasCancellationToken)
            {
                sb.AppendLine("            null,");
                sb.AppendLine($"            cancellationToken){resultSuffix};");
            }
            else
            {
                sb.AppendLine($"            null){resultSuffix};");
            }
        }
    }

    private static void GenerateQueryStringRequest(
        StringBuilder sb,
        string urlSegment,
        HttpMethod httpMethod,
        List<ParameterInfo> parameters,
        AsyncType asyncType,
        bool hasCancellationToken)
    {
        var verb = httpMethod.ToString();
        var awaitKeyword = GetAwaitKeyword(asyncType);
        var resultSuffix = GetResultSuffix(asyncType);

        if (parameters.Count > 0)
        {
            sb.AppendLine("        var queryString = System.Web.HttpUtility.ParseQueryString(string.Empty);");

            foreach (var parameter in parameters)
            {
                sb.Append("        queryString[\"")
                    .Append(parameter.Name.ToPascalCase())
                    .Append("\"] = ")
                    .Append(parameter.Name)
                    .AppendLine($"{(parameter.Type.EndsWith("?", StringComparison.Ordinal) ? "?" : string.Empty)}.ToString(System.Globalization.CultureInfo.InvariantCulture);");
            }

            sb.AppendLine($"        var response = {awaitKeyword}_httpClient.{verb}Async(");
            if (hasCancellationToken)
            {
                sb.AppendLine($"            $\"{{BaseRoute}}/{urlSegment}?{{queryString}}\",");
                sb.AppendLine($"            cancellationToken){resultSuffix};");
            }
            else
            {
                sb.AppendLine($"            $\"{{BaseRoute}}/{urlSegment}?{{queryString}}\"){resultSuffix};");
            }
        }
        else
        {
            sb.AppendLine($"        var response = {awaitKeyword}_httpClient.{verb}Async(");
            if (hasCancellationToken)
            {
                sb.AppendLine($"            $\"{{BaseRoute}}/{urlSegment}\",");
                sb.AppendLine($"            cancellationToken){resultSuffix};");
            }
            else
            {
                sb.AppendLine($"            $\"{{BaseRoute}}/{urlSegment}\"){resultSuffix};");
            }
        }
    }

    private static void GenerateOtherRequest(
        StringBuilder sb,
        string urlSegment,
        HttpMethod httpMethod,
        bool hasParameters,
        AsyncType asyncType,
        bool hasCancellationToken)
    {
        var awaitKeyword = GetAwaitKeyword(asyncType);
        var resultSuffix = GetResultSuffix(asyncType);

        sb.AppendLine("        var requestMessage = new HttpRequestMessage");
        sb.AppendLine("        {");
        sb.AppendLine($"            Method = HttpMethod.{httpMethod},");

        if (hasParameters)
        {
            sb.AppendLine($"            RequestUri = new Uri($\"{{BaseRoute}}/{urlSegment}\", UriKind.Relative),");
            sb.AppendLine("            Content = JsonContent.Create(request)");
        }
        else
        {
            sb.AppendLine($"            RequestUri = new Uri($\"{{BaseRoute}}/{urlSegment}\", UriKind.Relative)");
        }

        sb.AppendLine("        };");

        if (hasCancellationToken)
            sb.AppendLine($"        var response = {awaitKeyword}_httpClient.SendAsync(requestMessage, cancellationToken){resultSuffix};");
        else
            sb.AppendLine($"        var response = {awaitKeyword}_httpClient.SendAsync(requestMessage){resultSuffix};");
    }

    private static void GenerateStreamingClientMethodBody(StringBuilder sb, MethodInfo method, RouteNaming routeNaming)
    {
        var urlSegment = method.CustomRoute != null
            ? RouteTemplateToInterpolation(method.CustomRoute)
            : method.Name.ApplyRouteNaming(routeNaming);

        var nonRouteParams = method.Parameters.Where(static p => !p.IsRouteParameter).ToList();

        if (method.HttpMethod is HttpMethod.Get or HttpMethod.Delete)
        {
            GenerateStreamingQueryStringRequest(sb, urlSegment, method.HttpMethod, nonRouteParams);
        }
        else
        {
            if (nonRouteParams.Count > 0)
                BuildRequestObject(sb, nonRouteParams);
            GenerateStreamingOtherRequest(sb, urlSegment, method.HttpMethod, nonRouteParams);
        }

        sb.AppendLine();
        sb.AppendLine("        if (!response.IsSuccessStatusCode)");
        sb.AppendLine("        {");
        sb.AppendLine("            var errorBody = await response.Content.ReadAsStringAsync(cancellationToken);");
        sb.AppendLine("            throw new HttpRequestException(errorBody, null, response.StatusCode);");
        sb.AppendLine("        }");
        sb.AppendLine();
        sb.AppendLine($"        await foreach (var item in response.Content.ReadFromJsonAsAsyncEnumerable<{method.ReturnType}>(cancellationToken)!)");
        sb.AppendLine("            yield return item!;");
    }

    private static void GenerateStreamingQueryStringRequest(
        StringBuilder sb,
        string urlSegment,
        HttpMethod httpMethod,
        List<ParameterInfo> nonRouteParams)
    {
        var verb = httpMethod.ToString();

        if (nonRouteParams.Count > 0)
        {
            sb.AppendLine("        var queryString = System.Web.HttpUtility.ParseQueryString(string.Empty);");
            foreach (var parameter in nonRouteParams)
            {
                sb.Append("        queryString[\"")
                    .Append(parameter.Name.ToPascalCase())
                    .Append("\"] = ")
                    .Append(parameter.Name)
                    .AppendLine($"{(parameter.Type.EndsWith("?", StringComparison.Ordinal) ? "?" : string.Empty)}.ToString(System.Globalization.CultureInfo.InvariantCulture);");
            }

            sb.AppendLine($"        var response = await _httpClient.{verb}Async(");
            sb.AppendLine($"            $\"{{BaseRoute}}/{urlSegment}?{{queryString}}\",");
        }
        else
        {
            sb.AppendLine($"        var response = await _httpClient.{verb}Async(");
            sb.AppendLine($"            $\"{{BaseRoute}}/{urlSegment}\",");
        }

        sb.AppendLine("            HttpCompletionOption.ResponseHeadersRead,");
        sb.AppendLine("            cancellationToken);");
    }

    private static void GenerateStreamingOtherRequest(
        StringBuilder sb,
        string urlSegment,
        HttpMethod httpMethod,
        List<ParameterInfo> nonRouteParams)
    {
        sb.AppendLine("        var requestMessage = new HttpRequestMessage");
        sb.AppendLine("        {");
        sb.AppendLine($"            Method = HttpMethod.{httpMethod},");

        if (nonRouteParams.Count > 0)
        {
            sb.AppendLine($"            RequestUri = new Uri($\"{{BaseRoute}}/{urlSegment}\", UriKind.Relative),");
            sb.AppendLine("            Content = JsonContent.Create(request)");
        }
        else
        {
            sb.AppendLine($"            RequestUri = new Uri($\"{{BaseRoute}}/{urlSegment}\", UriKind.Relative)");
        }

        sb.AppendLine("        };");
        sb.AppendLine("        var response = await _httpClient.SendAsync(requestMessage, HttpCompletionOption.ResponseHeadersRead, cancellationToken);");
    }

    private static string FormatDefaultValue(string type, object? defaultValue)
    {
        if (defaultValue == null)
        {
            return "null";
        }

        if (string.Equals(type, "string", StringComparison.OrdinalIgnoreCase))
        {
            return $"\"{defaultValue}\"";
        }

        if (string.Equals(type, "bool", StringComparison.OrdinalIgnoreCase))
        {
            return defaultValue.ToString()?.ToLowerInvariant() ?? false.ToString();
        }

        if (type.Contains('?') && string.Equals(defaultValue.ToString(), "null", StringComparison.OrdinalIgnoreCase))
        {
            return "null";
        }

        return Convert.ToString(defaultValue, System.Globalization.CultureInfo.InvariantCulture)!;
    }
}
