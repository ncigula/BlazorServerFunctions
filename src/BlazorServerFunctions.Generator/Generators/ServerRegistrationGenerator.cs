using System.Text;
using BlazorServerFunctions.Generator.Models;

namespace BlazorServerFunctions.Generator.Generators;

internal sealed class ServerRegistrationGenerator
{
    private readonly IReadOnlyCollection<InterfaceInfo> _interfaces;
    private readonly string? _targetNamespace;
    private readonly StringBuilder _stringBuilder = new();

    private ServerRegistrationGenerator(IReadOnlyCollection<InterfaceInfo> interfaces, string? targetNamespace)
    {
        _interfaces = interfaces;
        _targetNamespace = targetNamespace;
    }

    public static string Generate(IReadOnlyCollection<InterfaceInfo> interfaces, string? targetNamespace) =>
        new ServerRegistrationGenerator(interfaces, targetNamespace).GenerateCore();

    private string GenerateCore()
    {
        var restInterfaces = _interfaces.Where(i => i.Configuration.ApiType != ApiType.GRPC).ToList();
        var grpcInterfaces = _interfaces.Where(i => i.Configuration.ApiType == ApiType.GRPC).ToList();

        if (restInterfaces.Count == 0 && grpcInterfaces.Count == 0)
            return string.Empty;

        // Use REST namespace first, fall back to gRPC namespace
        string fallbackNs = restInterfaces.Count > 0 ? restInterfaces[0].Namespace : grpcInterfaces[0].Namespace;
        var ns = !string.IsNullOrEmpty(_targetNamespace) ? _targetNamespace : fallbackNs;

        var externalNamespaces = _interfaces
            .Select(i => i.Namespace)
            .Where(n => !string.Equals(n, ns, StringComparison.Ordinal) && !string.IsNullOrEmpty(n))
            .Distinct(StringComparer.Ordinal)
            .OrderBy(n => n, StringComparer.Ordinal)
            .ToList();

        _stringBuilder.AppendLine("// <auto-generated />");
        _stringBuilder.AppendLine("#nullable enable");
        _stringBuilder.AppendLine();
        _stringBuilder.AppendLine("using Microsoft.AspNetCore.Builder;");
        _stringBuilder.AppendLine("using Microsoft.AspNetCore.Diagnostics.HealthChecks;");
        _stringBuilder.AppendLine("using Microsoft.AspNetCore.Routing;");
        _stringBuilder.AppendLine("using Microsoft.Extensions.DependencyInjection;");
        _stringBuilder.AppendLine("using Microsoft.Extensions.Diagnostics.HealthChecks;");
        // MapGrpcService<T> is an extension on IEndpointRouteBuilder from Grpc.AspNetCore.Server,
        // resolved via the Microsoft.AspNetCore.App framework reference — no extra using needed.
        _stringBuilder.AppendLine("using System;");
        _stringBuilder.AppendLine("using System.Threading;");
        _stringBuilder.AppendLine("using System.Threading.Tasks;");

        foreach (var externalNs in externalNamespaces)
            _stringBuilder.Append("using ").Append(externalNs).AppendLine(";");

        _stringBuilder.AppendLine();
        if (!string.IsNullOrEmpty(ns))
        {
            _stringBuilder.Append("namespace ").Append(ns).AppendLine(";");
            _stringBuilder.AppendLine();
        }

        _stringBuilder.AppendLine("public static class ServerFunctionEndpointsRegistration");
        _stringBuilder.AppendLine("{");
        GenerateMapEndpointsMethod(restInterfaces, grpcInterfaces);
        GenerateAddHealthChecksMethod();
        GenerateMapHealthChecksMethod();
        _stringBuilder.AppendLine("}");
        _stringBuilder.AppendLine();
        GenerateResolveCheckHelper();

        return _stringBuilder.ToString();
    }

    private void GenerateMapEndpointsMethod(List<InterfaceInfo> restInterfaces, List<InterfaceInfo> grpcInterfaces)
    {
        _stringBuilder.AppendLine("    public static IEndpointRouteBuilder MapServerFunctionEndpoints(");
        _stringBuilder.AppendLine("        this IEndpointRouteBuilder endpoints)");
        _stringBuilder.AppendLine("    {");

        foreach (var interfaceInfo in restInterfaces)
        {
            _stringBuilder.Append("        endpoints.Map")
                .Append(interfaceInfo.Name)
                .AppendLine("Endpoints();");
        }

        foreach (var grpcInterface in grpcInterfaces)
        {
            var serviceClassName = grpcInterface.Name.TrimStart('I') + "GrpcService";
            _stringBuilder.Append("        endpoints.MapGrpcService<")
                .Append(serviceClassName)
                .Append(">().EnableGrpcWeb()");
            if (grpcInterface.RequireAuthorization)
                _stringBuilder.Append(".RequireAuthorization()");
            _stringBuilder.AppendLine(";");
        }

        _stringBuilder.AppendLine("        return endpoints;");
        _stringBuilder.AppendLine("    }");
    }

    private void GenerateAddHealthChecksMethod()
    {
        _stringBuilder.AppendLine();
        _stringBuilder.AppendLine("    public static IServiceCollection AddServerFunctionHealthChecks(");
        _stringBuilder.AppendLine("        this IServiceCollection services)");
        _stringBuilder.AppendLine("    {");
        _stringBuilder.Append("        services.AddHealthChecks()");

        var list = _interfaces.ToList();
        for (var i = 0; i < list.Count; i++)
        {
            var tag = DeriveTag(list[i].Name);
            _stringBuilder.AppendLine();
            _stringBuilder.AppendLine("            .Add(new HealthCheckRegistration(");
            _stringBuilder.Append("                \"").Append(tag).AppendLine("\",");
            _stringBuilder.Append("                sp => new __BsfResolveCheck<").Append(list[i].Name).AppendLine(">(sp),");
            _stringBuilder.AppendLine("                HealthStatus.Unhealthy,");
            _stringBuilder.Append("                tags: new[] { \"bsf\" }))");
            if (i == list.Count - 1)
                _stringBuilder.AppendLine(";");
        }

        _stringBuilder.AppendLine("        return services;");
        _stringBuilder.AppendLine("    }");
    }

    private void GenerateMapHealthChecksMethod()
    {
        _stringBuilder.AppendLine();
        _stringBuilder.AppendLine("    public static IEndpointRouteBuilder MapServerFunctionHealthChecks(");
        _stringBuilder.AppendLine("        this IEndpointRouteBuilder endpoints,");
        _stringBuilder.AppendLine("        string pattern = \"/health/server-functions\")");
        _stringBuilder.AppendLine("    {");
        _stringBuilder.AppendLine("        endpoints.MapHealthChecks(pattern, new HealthCheckOptions");
        _stringBuilder.AppendLine("        {");
        _stringBuilder.AppendLine("            Predicate = check => check.Tags.Contains(\"bsf\"),");
        _stringBuilder.AppendLine("        });");
        _stringBuilder.AppendLine("        return endpoints;");
        _stringBuilder.AppendLine("    }");
    }

    private void GenerateResolveCheckHelper()
    {
        _stringBuilder.AppendLine("internal sealed class __BsfResolveCheck<TService> : IHealthCheck");
        _stringBuilder.AppendLine("    where TService : class");
        _stringBuilder.AppendLine("{");
        _stringBuilder.AppendLine("    private readonly IServiceProvider _sp;");
        _stringBuilder.AppendLine("    internal __BsfResolveCheck(IServiceProvider sp) => _sp = sp;");
        _stringBuilder.AppendLine();
        _stringBuilder.AppendLine("    public Task<HealthCheckResult> CheckHealthAsync(");
        _stringBuilder.AppendLine("        HealthCheckContext context,");
        _stringBuilder.AppendLine("        CancellationToken cancellationToken = default)");
        _stringBuilder.AppendLine("    {");
        _stringBuilder.AppendLine("        try");
        _stringBuilder.AppendLine("        {");
        _stringBuilder.AppendLine("            using var scope = _sp.CreateScope();");
        _stringBuilder.AppendLine("            _ = scope.ServiceProvider.GetRequiredService<TService>();");
        _stringBuilder.AppendLine("            return Task.FromResult(HealthCheckResult.Healthy());");
        _stringBuilder.AppendLine("        }");
        _stringBuilder.AppendLine("        catch (Exception ex)");
        _stringBuilder.AppendLine("        {");
        _stringBuilder.AppendLine("            return Task.FromResult(HealthCheckResult.Unhealthy(ex.Message, ex));");
        _stringBuilder.AppendLine("        }");
        _stringBuilder.AppendLine("    }");
        _stringBuilder.AppendLine("}");
    }

    private static string DeriveTag(string interfaceName) =>
        interfaceName.Length > 1 && interfaceName[0] == 'I' && char.IsUpper(interfaceName[1])
            ? interfaceName.Substring(1)
            : interfaceName;
}
