using System.Text;
using BlazorServerFunctions.Generator.Models;

namespace BlazorServerFunctions.Generator.Generators;

internal static class ServerRegistrationGenerator
{
    public static string Generate(IReadOnlyCollection<InterfaceInfo> interfaces)
    {
        if (interfaces.Count == 0)
            return string.Empty;

        var ns = GetCommonNamespace(interfaces);
        var sb = new StringBuilder();

        sb.AppendLine("// <auto-generated />");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using Microsoft.AspNetCore.Builder;");
        sb.AppendLine("using Microsoft.AspNetCore.Routing;");
        sb.AppendLine();
        sb.Append("namespace ").Append(ns).AppendLine(";");
        sb.AppendLine();
        sb.AppendLine("public static class ServerFunctionEndpointsRegistration");
        sb.AppendLine("{");
        sb.AppendLine("    public static IEndpointRouteBuilder MapServerFunctionEndpoints(");
        sb.AppendLine("        this IEndpointRouteBuilder endpoints)");
        sb.AppendLine("    {");

        foreach (var interfaceInfo in interfaces)
        {
            sb.Append("        endpoints.Map")
                .Append(interfaceInfo.Name)
                .AppendLine("Endpoints();");
        }

        sb.AppendLine("        return endpoints;");
        sb.AppendLine("    }");
        sb.AppendLine("}");

        return sb.ToString();
    }

    private static string GetCommonNamespace(IReadOnlyCollection<InterfaceInfo> interfaces)
    {
        if (interfaces.Count == 1)
            return interfaces.First().Namespace;

        var parts = interfaces
            .Select(i => i.Namespace.Split('.'))
            .ToList();

        var commonParts = new List<string>();
        var shortest = parts.Min(p => p.Length);

        for (var i = 0; i < shortest; i++)
        {
            var segment = parts[0][i];
            if (parts.All(p => string.Equals(p[i], segment, StringComparison.Ordinal)))
                commonParts.Add(segment);
            else
                break;
        }

        return commonParts.Count > 0
            ? string.Join(".", commonParts)
            : "BlazorServerFunctions.Generated";
    }
}
